use crate::canvas::{Stencil, Stencil14x32, Stencil5x7, Stencil8x16};

pub trait Font<const W: usize, const H: usize> {
    fn stencil_for(&self, c: char) -> &'static Stencil<W, H>;
}

pub struct Font5x7;

impl Font<5, 7> for Font5x7 {
    fn stencil_for(&self, c: char) -> &'static Stencil5x7 {
        match c {
            ' ' => &Self::EMPTY,
            '!' => &Self::EXCLAMATION,
            '"' => &Self::DOUBLE_QUOTE,
            '#' => &Self::HASH,
            '$' => &Self::DOLLAR,
            '%' => &Self::PERCENT,
            '&' => &Self::AMPERSAND,
            '\'' => &Self::QUOTE,
            '(' => &Self::PAREN_LEFT,
            ')' => &Self::PAREN_RIGHT,
            '*' => &Self::ASTERISK,
            '+' => &Self::PLUS,
            ',' => &Self::COMMA,
            '-' => &Self::HYPHEN,
            '.' => &Self::PERIOD,
            '/' => &Self::SLASH,
            '0' => &Self::N_0,
            '1' => &Self::N_1,
            '2' => &Self::N_2,
            '3' => &Self::N_3,
            '4' => &Self::N_4,
            '5' => &Self::N_5,
            '6' => &Self::N_6,
            '7' => &Self::N_7,
            '8' => &Self::N_8,
            '9' => &Self::N_9,
            ':' => &Self::S_COLON,
            ';' => &Self::SEMICOLON,
            '<' => &Self::LESS_THAN,
            '=' => &Self::EQUALS,
            '>' => &Self::GREATER_THAN,
            '?' => &Self::QUESTION,
            '@' => &Self::AT_SIGN,
            'a' | 'A' => &Self::A,
            'b' | 'B' => &Self::B,
            'c' | 'C' => &Self::C,
            'd' | 'D' => &Self::D,
            'e' | 'E' => &Self::E,
            'f' | 'F' => &Self::F,
            'g' | 'G' => &Self::G,
            'h' | 'H' => &Self::H,
            'i' | 'I' => &Self::I,
            'j' | 'J' => &Self::J,
            'k' | 'K' => &Self::K,
            'l' | 'L' => &Self::L,
            'm' | 'M' => &Self::M,
            'n' | 'N' => &Self::N,
            'o' | 'O' => &Self::O,
            'p' | 'P' => &Self::P,
            'q' | 'Q' => &Self::Q,
            'r' | 'R' => &Self::R,
            's' | 'S' => &Self::S,
            't' | 'T' => &Self::T,
            'u' | 'U' => &Self::U,
            'v' | 'V' => &Self::V,
            'w' | 'W' => &Self::W,
            'x' | 'X' => &Self::X,
            'y' | 'Y' => &Self::Y,
            'z' | 'Z' => &Self::Z,
            '[' => &Self::BRACKET_LEFT,
            '\\' => &Self::BACKSLASH,
            ']' => &Self::BRACKET_RIGHT,
            '^' => &Self::CARET,
            '_' => &Self::UNDERSCORE,
            '`' => &Self::BACKTICK,
            '{' => &Self::BRACE_LEFT,
            '|' => &Self::PIPE,
            '}' => &Self::BRACE_RIGHT,
            '~' => &Self::TILDE,
            _ => &Self::ANY,
        }
    }
}

